export type TranslationKey = keyof typeof translations.en;
export type Translator = (key: TranslationKey, values?: Record<string, number | string>) => string;

export function createTranslator(language: UiLanguage): Translator {
  return (key, values = {}) => {
    const template = String(translations[language][key] ?? translations.en[key] ?? key);
    return Object.entries(values).reduce<string>(
      (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
      template
    );
  };
}

export function rangeLabelKey(mode: CleanupTimeRangeMode): TranslationKey {
  return `range.${mode}` as TranslationKey;
}

export function formatCount(language: UiLanguage, kind: "historyRecord", count: number): string {
  if (kind === "historyRecord") {
    return language === "zh" ? `${count} 条历史记录` : `${count} history record${count === 1 ? "" : "s"}`;
  }
  return String(count);
}

export function formatHistoryTime(language: UiLanguage, time: number): string {
  if (!time) return createTranslator(language)("history.unknownTime");
  return new Intl.DateTimeFormat(language === "zh" ? "zh-CN" : "en-US", {
    month: "short",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit"
  }).format(time);
}

export function historyEntryWord(language: UiLanguage, count: number): string {
  if (language === "zh") return "";
  return count === 1 ? "entry" : "entries";
}

